async function makingListOfReferences(doi) {
    let arr = doi.split('\n')
    for(i of arr){

        try {
            await fetch(`https://cdn.jsdelivr.net/npm/citation-js`);
            let Cite = require('citation-js')
            let example = await new Cite(i)
    
            let output = example.format('bibliography', {
                format: 'text',
                template: 'apa',
                lang: 'en-US'
            })
            console.log(output)
        } catch(e){
            console.log(i+'\n')
        }
    }
}
